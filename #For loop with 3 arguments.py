#For loop with 3 arguments
#the print function will print 2 and incremental value increased by 2 and keep printing until it reaches 20
for i in range(2, 20, 3):
    print(i)

#note the following code will not print anything because the range 2nd value must always be greater than the 1st.
for n in range (4, 1):
    print(n)
